class Solution {
    public:
        bool lemonadeChange(vector<int>& bills) {
            int five = 0 , ten = 0;
    
            for(int i = 0 ; i < bills.size() ; i++){
                int bill = bills[i];
                if(bill == 5) five++;
    
                else if(bill == 10){
                    if(!five) return false;
                    
                    five--;
                    ten++;
                }
    
                else{
                    if(ten && five){
                        ten--;
                        five--;
                    }
                    else if(five >= 3) five -= 3;
    
                    else return false;
                }
            }
    
            return true;
        }
    };